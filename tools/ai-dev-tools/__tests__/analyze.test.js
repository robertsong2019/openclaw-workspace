// Helper: create a temp file and return its path
async function createTempFile(content, ext = '.js') {
  const tmpDir = await fs.mkdtemp(path.join(os.tmpdir(), 'aid-test-'));
  const filePath = path.join(tmpDir, `test${ext}`);
  await fs.writeFile(filePath, content, 'utf-8');
  return { filePath, cleanup: () => fs.remove(tmpDir) };
}

